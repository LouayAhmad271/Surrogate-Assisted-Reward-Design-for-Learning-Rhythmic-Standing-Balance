@dataclass
class RewardParams:
    # 9 weights
    w_stab: float
    w_freq: float
    w_move: float
    w_sway: float
    w_smooth: float
    w_torque: float
    w_vel: float
    w_ent: float
    w_target: float

    # 4 targets
    f_target: float
    v_target: float
    L_target: float
    S_target: float

    @classmethod
    def from_array(cls, arr: np.ndarray) -> "RewardParams":
        arr = np.asarray(arr, dtype=float).flatten()
        if arr.size != 13:
            raise ValueError(f"Expected 13 parameters, got {arr.size}")
        return cls(*arr.tolist())

    def to_array(self) -> np.ndarray:
        return np.array(list(asdict(self).values()), dtype=float)


@dataclass
class Trajectory:
    t: np.ndarray
    x: np.ndarray
    y: np.ndarray
    theta: np.ndarray
    phi: np.ndarray
    theta_dot: np.ndarray
    phi_dot: np.ndarray


@dataclass
class TrajectoryMetrics:
    mean_abs_velocity: float
    dominant_frequency: float
    spectral_centroid: float
    entropy: float
    path_length: float
    sway_area: float


