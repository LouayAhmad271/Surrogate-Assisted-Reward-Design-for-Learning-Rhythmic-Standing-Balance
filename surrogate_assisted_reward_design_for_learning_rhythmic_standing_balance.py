PARAM_NAMES = [
    "w_stab", "w_freq", "w_move", "w_sway", "w_smooth", "w_torque",
    "w_vel", "w_ent", "w_target", "f_target", "v_target", "L_target", "S_target"
]

BOUNDS = np.array([
    [0.1, 10.0],    # w_stab
    [0.1, 10.0],    # w_freq
    [0.0, 5.0],     # w_move
    [0.0, 5.0],     # w_sway
    [0.1, 5.0],     # w_smooth
    [0.0, 2.0],     # w_torque
    [0.0, 2.0],     # w_vel
    [0.0, 2.0],     # w_ent
    [0.0, 5.0],     # w_target
    [0.001, 1.0],   # f_target
    [0.01, 0.2],    # v_target
    [10.0, 2000.0], # L_target
    [1e-4, 1e-1],   # S_target
], dtype=float)


def sample_candidate(rng: np.random.Generator, human_targets: Dict[str, float]) -> np.ndarray:
    cand = np.zeros(len(PARAM_NAMES), dtype=float)

    # Weights: broad uniform sampling
    for i in range(9):
        low, high = BOUNDS[i]
        cand[i] = rng.uniform(low, high)

    # Targets: centered near human statistics, clipped to bounds
    target_map = {
        9: human_targets["dominant_frequency"],
        10: human_targets["mean_abs_velocity"],
        11: human_targets["path_length"],
        12: human_targets["sway_area"],
    }
    for idx, center in target_map.items():
        low, high = BOUNDS[idx]
        scale = 0.35 if idx in (9, 10) else 0.50
        val = rng.normal(loc=center, scale=max(abs(center) * scale, 1e-6))
        cand[idx] = np.clip(val, low, high)

    return cand


def candidate_to_params(candidate: np.ndarray) -> RewardParams:
    return RewardParams.from_array(candidate)


def surrogate_optimization_pipeline(
    human_metrics: List[TrajectoryMetrics],
    total_timesteps: int = PPO_TOTAL_TIMESTEPS,
    final_timesteps: int = PPO_FINAL_TIMESTEPS,
    seed: int = SEED,
) -> Tuple[RewardParams, PPO, pd.DataFrame]:
    rng = np.random.default_rng(seed)
    human_targets = summarize_human_targets(human_metrics)

    X = []
    y = []
    scores = []

    print("Initializing surrogate-assisted optimization pipeline...")
    print("Human reference targets:")
    print(json.dumps(human_targets, indent=2))

    # Initial design
    print(f"\nStage 1: Initial design (n={INITIAL_DESIGN_N})")
    for i in range(INITIAL_DESIGN_N):
        cand = sample_candidate(rng, human_targets)
        params = candidate_to_params(cand)
        score, _, summary = train_evaluate_ppo(params, total_timesteps=total_timesteps, seed=seed + i)

        X.append(cand)
        y.append(score)
        scores.append(summary)

        print(f"  Initial config {i + 1}/{INITIAL_DESIGN_N} | score={score:.3f} | "
              f"reward={summary['mean_episode_reward']:.3f} | steps={summary['mean_survival_steps']:.1f}")

    X = np.array(X, dtype=float)
    y = np.array(y, dtype=float)

    surrogate = RandomForestRegressor(
        n_estimators=300,
        random_state=seed,
        n_jobs=-1,
    )

    # Guided iterations
    print(f"\nStage 2: Guided iterations ({GUIDED_ITERS} rounds)")
    for it in range(GUIDED_ITERS):
        surrogate.fit(X, y)

        candidates = np.array([sample_candidate(rng, human_targets) for _ in range(CANDIDATES_PER_ITER)])
        pred = surrogate.predict(candidates)
        best_idx = int(np.argmax(pred))
        best_candidate = candidates[best_idx]

        actual_score, _, summary = train_evaluate_ppo(
            candidate_to_params(best_candidate),
            total_timesteps=total_timesteps,
            seed=seed + 100 + it,
        )

        X = np.vstack([X, best_candidate])
        y = np.append(y, actual_score)
        scores.append(summary)

        print(f"  Iteration {it + 1}/{GUIDED_ITERS} | "
              f"pred={pred[best_idx]:.3f} | actual={actual_score:.3f} | "
              f"reward={summary['mean_episode_reward']:.3f} | steps={summary['mean_survival_steps']:.1f}")

    best_idx = int(np.argmax(y))
    best_candidate = X[best_idx]
    best_params = candidate_to_params(best_candidate)

    print("\nOptimization complete.")
    print(f"Best observed score: {y[best_idx]:.3f}")
    print("Best parameter vector:")
    print(dict(zip(PARAM_NAMES, best_candidate.tolist())))

    # Final longer training run
    print(f"\nStage 3: Final training run ({final_timesteps} timesteps)")
    final_env = RhythmicStandingEnv(best_params)
    final_model = build_ppo_model(final_env, seed=seed)
    final_model.learn(total_timesteps=final_timesteps)

    # Save artifacts
    joblib.dump(surrogate, OUTPUT_DIR / "rf_surrogate.joblib")
    final_model.save(str(OUTPUT_DIR / "final_ppo_model"))
    with open(OUTPUT_DIR / "best_reward_params.json", "w", encoding="utf-8") as f:
        json.dump(
            {name: float(val) for name, val in zip(PARAM_NAMES, best_candidate.tolist())},
            f,
            indent=2,
        )
    pd.DataFrame(scores).to_csv(OUTPUT_DIR / "training_summaries.csv", index=False)

    return best_params, final_model, pd.DataFrame(scores)


