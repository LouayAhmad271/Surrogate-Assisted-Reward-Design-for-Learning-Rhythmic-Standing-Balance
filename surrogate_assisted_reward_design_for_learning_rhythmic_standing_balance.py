def set_global_seed(seed: int) -> None:
    np.random.seed(seed)
    torch.manual_seed(seed)


def clip_with_eps(x: np.ndarray, low: float, high: float) -> np.ndarray:
    return np.clip(x, low, high)


def shannon_entropy_from_counts(counts: np.ndarray) -> float:
    counts = np.asarray(counts, dtype=float)
    total = counts.sum()
    if total <= 0:
        return 0.0
    p = counts[counts > 0] / total
    return float(-np.sum(p * np.log2(p)))


def safe_convex_hull_area(x: np.ndarray, y: np.ndarray) -> float:
    pts = np.column_stack([x, y])
    if len(pts) < 3:
        return 0.0
    try:
        hull = ConvexHull(pts)
        return float(hull.volume)  # 2D hull area
    except Exception:
        return 0.0


def dominant_frequency_and_centroid(signal: np.ndarray, fs: float) -> Tuple[float, float]:
    signal = np.asarray(signal, dtype=float)
    if signal.size < 8:
        return 0.0, 0.0

    nperseg = min(256, signal.size)
    f, pxx = welch(signal, fs=fs, nperseg=nperseg, scaling="density")
    if pxx.size == 0 or np.all(pxx <= 0):
        return 0.0, 0.0

    # Ignore DC for dominant frequency
    if len(f) > 1:
        idx = np.argmax(pxx[1:]) + 1
    else:
        idx = 0
    dom_f = float(f[idx])

    p = np.maximum(pxx, 0)
    if np.sum(p) <= 0:
        centroid = 0.0
    else:
        centroid = float(np.sum(f * p) / np.sum(p))
    return dom_f, centroid


def compute_entropy_2d(theta: np.ndarray, phi: np.ndarray, bins: int = 16) -> float:
    theta = np.asarray(theta, dtype=float)
    phi = np.asarray(phi, dtype=float)
    if theta.size < 4 or phi.size < 4:
        return 0.0

    # Fixed range keeps the metric comparable across trajectories
    hist, _, _ = np.histogram2d(theta, phi, bins=bins, range=[(-0.5, 0.5), (-0.5, 0.5)])
    return shannon_entropy_from_counts(hist.ravel())


def finite_diff(x: np.ndarray, dt: float) -> np.ndarray:
    x = np.asarray(x, dtype=float)
    if x.size < 2:
        return np.zeros_like(x)
    return np.gradient(x, dt)


def mean_abs_angular_velocity(theta_dot: np.ndarray, phi_dot: np.ndarray) -> float:
    theta_dot = np.asarray(theta_dot, dtype=float)
    phi_dot = np.asarray(phi_dot, dtype=float)
    return float(np.mean((np.abs(theta_dot) + np.abs(phi_dot)) / 2.0))


def path_length(x: np.ndarray, y: np.ndarray) -> float:
    x = np.asarray(x, dtype=float)
    y = np.asarray(y, dtype=float)
    if x.size < 2:
        return 0.0
    dx = np.diff(x)
    dy = np.diff(y)
    return float(np.sum(np.hypot(dx, dy)))


