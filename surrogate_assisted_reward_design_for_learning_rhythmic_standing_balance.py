def load_trajectory_from_csv(path: Path, sample_rate: float = SAMPLE_RATE) -> Trajectory:
    df = pd.read_csv(path)

    def find_col(candidates: List[str]) -> Optional[str]:
        lower_map = {c.lower().strip(): c for c in df.columns}
        for cand in candidates:
            if cand.lower() in lower_map:
                return lower_map[cand.lower()]
        return None

    x_col = find_col(["x", "cop_x", "pos_x"])
    y_col = find_col(["y", "cop_y", "pos_y"])
    theta_col = find_col(["theta", "angle_x", "sagittal_angle"])
    phi_col = find_col(["phi", "angle_y", "frontal_angle"])
    time_col = find_col(["t", "time", "timestamp"])

    if x_col is None or y_col is None:
        raise ValueError(f"{path.name}: could not find x/y COP columns.")

    x = df[x_col].to_numpy(dtype=float)
    y = df[y_col].to_numpy(dtype=float)

    if time_col is not None:
        t = df[time_col].to_numpy(dtype=float)
        if t.size >= 2:
            dt = float(np.median(np.diff(t)))
        else:
            dt = 1.0 / sample_rate
    else:
        dt = 1.0 / sample_rate
        t = np.arange(len(x)) * dt

    L = 0.85
    if theta_col is not None:
        theta = df[theta_col].to_numpy(dtype=float)
    else:
        theta = np.arcsin(np.clip(x / L, -1.0, 1.0))

    if phi_col is not None:
        phi = df[phi_col].to_numpy(dtype=float)
    else:
        phi = np.arcsin(np.clip(y / L, -1.0, 1.0))

    theta_dot = finite_diff(theta, dt)
    phi_dot = finite_diff(phi, dt)
    return Trajectory(t=t, x=x, y=y, theta=theta, phi=phi, theta_dot=theta_dot, phi_dot=phi_dot)


def load_human_trajectories(data_dir: Path) -> List[Trajectory]:
    if not data_dir.exists():
        raise FileNotFoundError(f"Human data directory not found: {data_dir.resolve()}")

    files = sorted(list(data_dir.glob("*.csv")))
    if not files:
        raise FileNotFoundError(
            f"No CSV files found in {data_dir.resolve()}. "
            "Place the human COP trajectories there."
        )

    trajectories = []
    for fp in files:
        traj = load_trajectory_from_csv(fp)
        trajectories.append(traj)
    return trajectories


def trajectory_metrics(traj: Trajectory, sample_rate: float = SAMPLE_RATE) -> TrajectoryMetrics:
    mav = mean_abs_angular_velocity(traj.theta_dot, traj.phi_dot)
    dom_f, centroid = dominant_frequency_and_centroid(traj.theta, fs=sample_rate)
    ent = compute_entropy_2d(traj.theta, traj.phi, bins=16)
    plen = path_length(traj.x, traj.y)
    area = safe_convex_hull_area(traj.x, traj.y)
    return TrajectoryMetrics(
        mean_abs_velocity=mav,
        dominant_frequency=dom_f,
        spectral_centroid=centroid,
        entropy=ent,
        path_length=plen,
        sway_area=area,
    )


def metrics_from_arrays(
    theta: np.ndarray,
    phi: np.ndarray,
    x: np.ndarray,
    y: np.ndarray,
    dt: float = DT,
) -> TrajectoryMetrics:
    theta = np.asarray(theta, dtype=float)
    phi = np.asarray(phi, dtype=float)
    x = np.asarray(x, dtype=float)
    y = np.asarray(y, dtype=float)
    theta_dot = finite_diff(theta, dt)
    phi_dot = finite_diff(phi, dt)
    traj = Trajectory(
        t=np.arange(len(x)) * dt,
        x=x,
        y=y,
        theta=theta,
        phi=phi,
        theta_dot=theta_dot,
        phi_dot=phi_dot,
    )
    return trajectory_metrics(traj)


def summarize_human_targets(metrics_list: List[TrajectoryMetrics]) -> Dict[str, float]:
    arr = {
        "mean_abs_velocity": np.array([m.mean_abs_velocity for m in metrics_list], dtype=float),
        "dominant_frequency": np.array([m.dominant_frequency for m in metrics_list], dtype=float),
        "spectral_centroid": np.array([m.spectral_centroid for m in metrics_list], dtype=float),
        "entropy": np.array([m.entropy for m in metrics_list], dtype=float),
        "path_length": np.array([m.path_length for m in metrics_list], dtype=float),
        "sway_area": np.array([m.sway_area for m in metrics_list], dtype=float),
    }
    return {k: float(np.mean(v)) for k, v in arr.items()}


