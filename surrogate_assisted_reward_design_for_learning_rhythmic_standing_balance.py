def build_ppo_model(env: gym.Env, seed: int = SEED) -> PPO:
    policy_kwargs = dict(
        activation_fn=nn.Tanh,
        net_arch=dict(pi=[256, 256], vf=[256, 256]),
    )

    model = PPO(
        "MlpPolicy",
        env,
        gamma=0.995,
        gae_lambda=0.97,
        clip_range=0.2,
        learning_rate=3e-4,
        ent_coef=0.008,
        n_epochs=8,
        batch_size=256,
        max_grad_norm=0.6,
        policy_kwargs=policy_kwargs,
        verbose=0,
        seed=seed,
        device="auto",
    )
    return model


def rollout_episode(model: PPO, env: gym.Env, deterministic: bool = True) -> Tuple[Trajectory, float, int]:
    obs, _ = env.reset()
    done = False
    total_reward = 0.0
    steps = 0

    xs, ys, thetas, phis = [], [], [], []

    while not done:
        action, _ = model.predict(obs, deterministic=deterministic)
        obs, reward, terminated, truncated, _ = env.step(action)

        total_reward += float(reward)
        steps += 1

        theta, phi, dot_theta, dot_phi, psi, x, y, v_x, v_y = env.state
        xs.append(x)
        ys.append(y)
        thetas.append(theta)
        phis.append(phi)

        done = bool(terminated or truncated)

    traj = Trajectory(
        t=np.arange(steps) * env.dt,
        x=np.array(xs, dtype=float),
        y=np.array(ys, dtype=float),
        theta=np.array(thetas, dtype=float),
        phi=np.array(phis, dtype=float),
        theta_dot=finite_diff(np.array(thetas, dtype=float), env.dt),
        phi_dot=finite_diff(np.array(phis, dtype=float), env.dt),
    )
    return traj, total_reward, steps


def train_evaluate_ppo(
    reward_params: RewardParams,
    total_timesteps: int = PPO_TOTAL_TIMESTEPS,
    eval_episodes: int = EVAL_EPISODES_FOR_SCORE,
    seed: int = SEED,
) -> Tuple[float, PPO, Dict[str, float]]:
    env = RhythmicStandingEnv(reward_params)
    model = build_ppo_model(env, seed=seed)
    model.learn(total_timesteps=total_timesteps)

    episode_rewards = []
    episode_steps = []

    for _ in range(eval_episodes):
        traj, total_reward, steps = rollout_episode(model, env, deterministic=True)
        episode_rewards.append(total_reward)
        episode_steps.append(steps)

    mean_reward = float(np.mean(episode_rewards))
    mean_steps = float(np.mean(episode_steps))

    # Fitness used by the surrogate search
    score = mean_reward + 0.1 * mean_steps

    eval_summary = {
        "mean_episode_reward": mean_reward,
        "mean_survival_steps": mean_steps,
        "fitness_score": float(score),
    }
    return float(score), model, eval_summary


