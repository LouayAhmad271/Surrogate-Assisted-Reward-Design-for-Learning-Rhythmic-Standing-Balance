class RhythmicStandingEnv(gym.Env):
    metadata = {"render_modes": []}

    def __init__(self, reward_params: RewardParams):
        super().__init__()

        self.g = 9.81
        self.L = 0.85
        self.M = 71.83
        self.I = (self.M * self.L**2) / 3.0
        self.beta = 4.0
        self.tau_max = 75.0
        self.dt = DT
        self.max_steps = MAX_STEPS

        # Oscillator constants
        self.A_theta = 0.08
        self.A_phi = 0.08
        self.alpha = 0.45
        self.gamma_osc = 0.08

        # Reward parameters
        self.reward_params = reward_params

        # Action space: normalized torques in [-1, 1]
        self.action_space = spaces.Box(low=-1.0, high=1.0, shape=(2,), dtype=np.float32)

        # Observation: 16 dims
        self.observation_space = spaces.Box(
            low=-np.inf, high=np.inf, shape=(16,), dtype=np.float32
        )

        self.state = None
        self.steps = 0
        self.prev_action = np.zeros(2, dtype=float)
        self.prev_x = 0.0
        self.prev_y = 0.0
        self.episode_x: List[float] = []
        self.episode_y: List[float] = []
        self.theta_window = deque(maxlen=64)
        self.phi_window = deque(maxlen=64)

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)

        self.steps = 0
        self.prev_action = np.zeros(2, dtype=float)

        theta = self.np_random.uniform(-0.01, 0.01)
        phi = self.np_random.uniform(-0.01, 0.01)
        dot_theta = self.np_random.uniform(-0.01, 0.01)
        dot_phi = self.np_random.uniform(-0.01, 0.01)
        psi = self.np_random.uniform(0, 2 * np.pi)
        x, y, v_x, v_y = 0.0, 0.0, 0.0, 0.0

        self.state = np.array([theta, phi, dot_theta, dot_phi, psi, x, y, v_x, v_y], dtype=float)
        self.prev_x = x
        self.prev_y = y
        self.episode_x = [x]
        self.episode_y = [y]
        self.theta_window.clear()
        self.phi_window.clear()
        self.theta_window.append(theta)
        self.phi_window.append(phi)

        return self._get_obs(), {}

    def step(self, action):
        action = np.asarray(action, dtype=float).flatten()
        action = np.clip(action, -1.0, 1.0)

        theta, phi, dot_theta, dot_phi, psi, x, y, v_x, v_y = self.state

        tau_theta = float(action[0] * self.tau_max)
        tau_phi = float(action[1] * self.tau_max)

        # Perturbations
        eps_theta = self.A_theta * np.sin(psi)
        eps_phi = self.A_phi * np.sin(psi + np.pi / 2.0)

        # Dynamics
        ddot_theta = (
            -(self.g / self.L) * np.sin(theta)
            + (tau_theta / self.I)
            - self.beta * dot_theta
            + eps_theta
        )
        ddot_phi = (
            -(self.g / self.L) * np.sin(phi)
            + (tau_phi / self.I)
            - self.beta * dot_phi
            + eps_phi
        )

        dot_psi = (
            2.0 * np.pi * self.reward_params.f_target
            + self.alpha * (abs(dot_theta) + abs(dot_phi))
            + self.gamma_osc * ((tau_theta - tau_phi) / self.tau_max)
        )

        # COP velocities
        v_x_new = 0.8 * v_x + 0.18 * abs(np.sin(psi)) + 0.05 * (abs(dot_theta) + abs(dot_phi))
        v_y_new = 0.7 * v_y + 0.03 * np.sin(psi + np.pi / 2.0) + 0.01 * (phi - theta)

        # Semi-implicit Euler
        theta = theta + dot_theta * self.dt
        phi = phi + dot_phi * self.dt
        dot_theta = dot_theta + ddot_theta * self.dt
        dot_phi = dot_phi + ddot_phi * self.dt
        psi = psi + dot_psi * self.dt
        x = x + v_x_new * self.dt
        y = y + v_y_new * self.dt

        self.state = np.array([theta, phi, dot_theta, dot_phi, psi, x, y, v_x_new, v_y_new], dtype=float)
        self.steps += 1

        self.episode_x.append(x)
        self.episode_y.append(y)
        self.theta_window.append(theta)
        self.phi_window.append(phi)

        fall_threshold = math.radians(FALL_THRESHOLD_DEG)
        terminated = bool(abs(theta) > fall_threshold or abs(phi) > fall_threshold)
        truncated = bool(self.steps >= self.max_steps)

        reward = self._calculate_reward(action, terminated, truncated, dot_psi, x, y, v_x_new, v_y_new)
        self.prev_action = action.copy()
        self.prev_x = x
        self.prev_y = y

        return self._get_obs(), float(reward), terminated, truncated, {}

    def _get_obs(self):
        theta, phi, dot_theta, dot_phi, psi, x, y, v_x, v_y = self.state
        time_frac = self.steps / self.max_steps

        return np.array(
            [
                theta, phi, dot_theta, dot_phi, psi, x, y, v_x, v_y,
                np.sin(theta), np.cos(theta),
                np.sin(phi), np.cos(phi),
                np.sin(psi), np.cos(psi),
                time_frac,
            ],
            dtype=np.float32,
        )

    def _reward_entropy(self) -> float:
        # Sliding-window entropy over posture angles
        theta_vals = np.array(self.theta_window, dtype=float)
        phi_vals = np.array(self.phi_window, dtype=float)
        if theta_vals.size < 6 or phi_vals.size < 6:
            return 0.0

        hist, _, _ = np.histogram2d(theta_vals, phi_vals, bins=8, range=[(-0.5, 0.5), (-0.5, 0.5)])
        return shannon_entropy_from_counts(hist.ravel())

    def _terminal_target_reward(self) -> float:
        # Match episode path length and sway area to targets
        x = np.array(self.episode_x, dtype=float)
        y = np.array(self.episode_y, dtype=float)
        plen = path_length(x, y)
        area = safe_convex_hull_area(x, y)

        L_t = self.reward_params.L_target
        S_t = self.reward_params.S_target

        r_L = np.exp(-((plen - L_t) / (abs(L_t) + 1e-6)) ** 2)
        r_S = np.exp(-((area - S_t) / (abs(S_t) + 1e-6)) ** 2)
        return self.reward_params.w_target * 0.5 * (r_L + r_S)

    def _calculate_reward(self, action, terminated, truncated, dot_psi, x, y, v_x, v_y):
        theta, phi, dot_theta, dot_phi, psi, _, _, _, _ = self.state

        p = self.reward_params

        # 1) Stability
        sigma = 0.1
        r_stab = p.w_stab * np.exp(-(theta**2 + phi**2) / (2.0 * sigma**2))

        # 2) Frequency matching
        current_f = dot_psi / (2.0 * np.pi)
        r_freq = p.w_freq * np.exp(-((current_f - p.f_target) / (abs(p.f_target) + 1e-6)) ** 2)

        # 3) Movement and sway
        delta_x = v_x * self.dt
        delta_y = v_y * self.dt
        r_move = -p.w_move * (abs(delta_x) + abs(delta_y))
        r_sway = p.w_sway * abs(delta_y)

        # 4) Smoothness
        action_diff = np.linalg.norm(action - self.prev_action)
        r_smooth = p.w_smooth * np.exp(-0.5 * action_diff**2)

        # 5) Torque and velocity penalties
        r_torque = -p.w_torque * (action[0] ** 2 + action[1] ** 2)
        r_vel = -p.w_vel * ((abs(dot_theta) + abs(dot_phi)) / 2.0)

        # 6) Entropy proxy
        r_ent = p.w_ent * self._reward_entropy()

        # 7) Velocity target matching
        v_mean = (abs(dot_theta) + abs(dot_phi)) / 2.0
        r_vtarget = 0.75 * np.exp(-((v_mean - p.v_target) / (abs(p.v_target) + 1e-6)) ** 2)

        # 8) Survival / fall logic
        r_survival = 1.0 if not terminated else -8.0

        # 9) Terminal summary-stat matching (path length + sway area)
        r_target = 0.0
        if terminated or truncated:
            r_target = self._terminal_target_reward()

        total_reward = (
            r_stab + r_freq + r_move + r_sway + r_smooth +
            r_torque + r_vel + r_ent + r_vtarget + r_survival + r_target
        )
        return float(total_reward)


