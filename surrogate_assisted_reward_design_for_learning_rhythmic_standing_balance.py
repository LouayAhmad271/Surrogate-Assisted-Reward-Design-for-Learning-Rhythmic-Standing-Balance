def compare_distributions(human_vals: np.ndarray, agent_vals: np.ndarray) -> Dict[str, object]:
    human_vals = np.asarray(human_vals, dtype=float)
    agent_vals = np.asarray(agent_vals, dtype=float)

    human_p = np.nan
    agent_p = np.nan
    human_normal = False
    agent_normal = False

    if len(human_vals) >= 3 and len(human_vals) <= 5000:
        try:
            human_p = shapiro(human_vals).pvalue
            human_normal = bool(human_p > 0.05)
        except Exception:
            human_p = np.nan

    if len(agent_vals) >= 3 and len(agent_vals) <= 5000:
        try:
            agent_p = shapiro(agent_vals).pvalue
            agent_normal = bool(agent_p > 0.05)
        except Exception:
            agent_p = np.nan

    if human_normal and agent_normal:
        test_name = "Welch_t"
        stat, p = ttest_ind(human_vals, agent_vals, equal_var=False, nan_policy="omit")
    else:
        test_name = "Mann_Whitney_U"
        stat, p = mannwhitneyu(human_vals, agent_vals, alternative="two-sided")

    return {
        "human_mean": float(np.mean(human_vals)),
        "agent_mean": float(np.mean(agent_vals)),
        "human_shapiro_p": None if np.isnan(human_p) else float(human_p),
        "agent_shapiro_p": None if np.isnan(agent_p) else float(agent_p),
        "test": test_name,
        "statistic": float(stat),
        "p_value": float(p),
        "significant": bool(p < 0.05),
    }


